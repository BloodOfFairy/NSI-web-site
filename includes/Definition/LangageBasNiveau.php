<h4 class="contentTitle">Les langages de bas niveau</h4><br>
		<p>
			Les langages de bas niveau dépendent des caractéristiques de l’ordinateur. Ils  sont donc aujourd'hui de moins en moins utilisés pour développer les applications courantes. La distinction entre bas niveau et haut niveau n'est pas binaire mais moins un language est proche des langues humains plus il seras de bas niveau.
		</p>